    /**
     * @param $seller
     * @return Order[] Returns an array of Order objects
     */
    public function findOrderBySeller($seller)
    {
        return $this->createQueryBuilder('o')
            ->select('o')
            ->join('o.carts','c')
            ->join('c.seller','s')
            ->where('s = :seller')
            ->setParameter('seller', $seller)
            ->getQuery()
            ->getResult()
            ;
    }

